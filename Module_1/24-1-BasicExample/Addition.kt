fun main()
{
    // variable declaration

    var a = 10
    var b = 11

    // Addition
    var c = a+b
    println("Addition of a and b is $c")

    //Substraction
    var d = a-b
    println("Substraction of a and b is $d")

    // Multiplication
    var e = a*b
    println("Multiplication of a and b is $e")

    //Division
    var f = a/b
    println("Division of a and b is $f")
}